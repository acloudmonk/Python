# Python Program for Creation of String

# Creating a String with single Quotes
message = "Welcome to the India"
print("String with the use of Single Quotes: " + message)
print(type(message))

# Creating a String with double Quotes
message = "I'm a youtuber"
print("String with the use of Double Quotes: " + message)
print(type(message))

# Creating a String with triple Quotes
message = '''I'm a DevOps Engineer and I live in "Chandigarh"'''
print("String with the use of Triple Quotes: " + message)
print(type(message))

# Creating String with triple Quotes allows multiple lines
message = """Ready to 
			live 
			Life"""
print("Creating a multiline String: " + message)
print(type(message))
